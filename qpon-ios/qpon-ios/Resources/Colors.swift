import Foundation
import UIKit

extension UIColor {
    
    class func primaryColor() -> UIColor {
        return UIColor(red: 0.929, green: 0.176, blue: 0.29, alpha: 1)
    }
    
    class func darkBlueColor() -> UIColor {
        return UIColor(red: 0.082, green: 0.102, blue: 0.275, alpha: 1)
    }
    
    class func darkRedColor() -> UIColor {
        return UIColor(red: 0.629, green: 0.121, blue: 0.197, alpha: 1)
    }
    
    class func orangeColor() -> UIColor {
        return UIColor(red: 0.937, green: 0.58, blue: 0.231, alpha: 1)
    }
    
    class func yellowColor() -> UIColor {
        return UIColor(red: 0.976, green: 0.816, blue: 0.365, alpha: 1)
    }
}
